package lesson6_1;

public class Animal {
    private String nickname;
    private int age;
    private String breed;
    private String color;
    private int trackLength;

    public Animal(String nickname, int age, String breed, String color, int trackLength) {
        this.nickname = nickname;
        this.age = age;
        this.breed = breed;
        this.color = color;
        this.trackLength = trackLength;
    }
}
